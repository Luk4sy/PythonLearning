# 定义一个列表list
my_list = ["lukasy", "java", "python"]
print(my_list)
print(type(my_list))

my_list = ["lukasy", 123, True]
print(my_list)
print(type(my_list))

# 定义一个嵌套列表
my_list = [[1, 2, 3], [4, 5, 6]]
print(my_list)
print(type(my_list))

# 通过下表索引出去对应位置的数据
my_list = ["Tom", "Lily", "Rose"]
print(my_list[0])
print(my_list[1])
print(my_list[2])

# 倒序取出
print(my_list[-1])
print(my_list[-2])
print(my_list[-3])
