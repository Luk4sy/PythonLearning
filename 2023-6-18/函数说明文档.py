def add(x,y):
    """
    add函数可以接收2个参数，进行两数相加的功能
    :param x: 形参x表示相加的其中一个数字
    :param y: 形参y表示相加的另一个数字
    :return: 返回值是两数相加的结果
    """

    result = x + y
    print(f"两数相加的结果是:{result}")
    return result

# 鼠标悬停在在函数名上可以看见函数文档说明
add(5,6)